public class StringsNumbers {
    public static void main(String[] args){
        System.out.print("6+9/2(3+2) = ");
        System.out.println(6+9/2.0*(3+2));
        System.out.print("7(6+22) = ");
        System.out.println(7*(6+22));
        System.out.print("32/(5/4) = ");
        System.out.println(32/(5.0/4));
        System.out.print("4-[6-(2-7)] = ");
        System.out.println(4-(6-(2-7)));
        System.out.print("5/(4*2) = ");
        System.out.println(5/(4.0*2));
        System.out.print("(7+1-3(5))/(4/5 +2) = ");
        System.out.print((7+1-(3*5))/(4.0/5+2));
    }
}
